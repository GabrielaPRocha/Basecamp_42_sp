int	ft_sqrt(int nb)
{
	int x;

	x = 0;
	if (nb == 2147483647)
	{
		return (0);
	}
	while (x <= nb && (x * x) < nb)
	{
		x++;
	}
	if ((x * x) == nb)
	{
		return (x);
	}
	else
	{
		return (0);
	}
}
